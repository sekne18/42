#include "fdf.h"

int	main(int argc, char **argv)
{
	char	**map;

	map = NULL;
	if (argc != 2)
		return (0);
	if (parse_map(&map, argv[1]) == 0)
		return (0);
	fdf(map);	
	free_all(map);
	return (0);
}
